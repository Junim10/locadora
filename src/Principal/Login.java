    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection con = Conexao.AbrirConexao();
        FuncionarioDAO sql = new FuncionarioDAO(con);
        String login = jtuser.getText();
        String senha = jtpass.getText();
        
        if (login.equalsIgnoreCase("") || senha.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Nenhum campo pode esta vazio",
                    "Video Locadora", JOptionPane.WARNING_MESSAGE);
                    
        }else{
            if(sql.Logar(login, senha) == true){
                new Thread(){
                    public void run (){
                        for (int i = 0; i < 101; i++){
                            jProgressBar1.setValue(i);
                            try {
                                Thread.sleep(35);
                            } catch (Exception e) {
                                e.getMessage();
                            }
                        }
                        new Menu().setVisible(true);
                        dispose();
                    }
                }.start();
            }else{
                JOptionPane.showMessageDialog(null, "Usúario ou senha invalido!",
                        "Video Locadora", JOptionPane.ERROR_MESSAGE);
                        jtuser.setText("");
                        jtpass.setText("");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

