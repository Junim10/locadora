    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        String nome = jTextField2.getText(); 
        
        if(nome.equalsIgnoreCase("")){
            
            JOptionPane.showMessageDialog(null, "Esse campo n√£o poderar esta vazio", "Video Locadora", JOptionPane.WARNING_MESSAGE);
            
        }else{
            Connection con = Conexao.AbrirConexao();
            CategoriaDAO sql = new CategoriaDAO(con);
            Categoria c = new Categoria();
                    
            c.setNome(nome);
            
            sql.Inserir_Categoria(c);
            
            Conexao.FecharConexao(con);
            
            jTextField2.setText("");
  
            JOptionPane.showMessageDialog(null, "Inserido com sucesso!", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton11ActionPerformed

