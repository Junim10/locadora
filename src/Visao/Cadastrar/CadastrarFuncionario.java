    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome = jtnome.getText(); 
        String login = jtlogin.getText(); 
        String senha = jtsenha.getText();
        
        if(nome.equalsIgnoreCase("") || login.equalsIgnoreCase("") || senha.equalsIgnoreCase("")){
            
            JOptionPane.showMessageDialog(null, "Nenhum campo poderar esta vazio", "Video Locadora", JOptionPane.WARNING_MESSAGE);
            
        }else{
            Connection con = Conexao.AbrirConexao();
            FuncionarioDAO sql = new FuncionarioDAO(con);
            Funcionario f = new Funcionario();
                    
            f.setNome(nome);
            f.setLogin(login);
            f.setSenha(senha);
            
            sql.Inserir_Funcionario(f);
            
            Conexao.FecharConexao(con);
            
            jtnome.setText("");
            jtlogin.setText("");
            jtsenha.setText("");
  
            JOptionPane.showMessageDialog(null, "Inseridos com sucesso!", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

