    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String nome = jTextField1.getText(); 
        String rg = jTextField2.getText(); 
        String cpf = jTextField3.getText();
        String tel = jTextField4.getText(); 
        String nasc = jTextField5.getText(); 
        String email = jTextField10.getText();
        String bairro = jTextField8.getText(); 
        String rua = jTextField6.getText(); 
        String cep = jTextField9.getText();
        String num = jTextField7.getText();
        
        if(nome.equalsIgnoreCase("") || rg.equalsIgnoreCase("") || cpf.equalsIgnoreCase("")
                || cep.equalsIgnoreCase("") || bairro.equalsIgnoreCase("") || rua.equalsIgnoreCase("") || email.equalsIgnoreCase("")
                || num.equalsIgnoreCase("") || tel.equalsIgnoreCase("") || nasc.equalsIgnoreCase("")){
            
            JOptionPane.showMessageDialog(null, "Nenhum campo poderar esta vazio", "Video Locadora", JOptionPane.WARNING_MESSAGE);
            
        }else{
            Connection con = Conexao.AbrirConexao();
            ClienteDAO sql = new ClienteDAO(con);
            int numero = Integer.parseInt(num);
            Cliente f = new Cliente();
                    
            f.setNome(nome);
            f.setNascimento(nasc);
            f.setNumero(numero);
            f.setBairro(bairro);
            f.setTelefone(tel);
            f.setEmail(email);
            f.setRua(rua);
            f.setCEP(cep);
            f.setCPF(cpf);
            f.setRG(rg);
            
            sql.Inserir_Cliente(f);
            
            Conexao.FecharConexao(con);
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
             jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
             jTextField7.setText("");
            jTextField8.setText("");
            jTextField9.setText("");
            jTextField10.setText("");
  
            JOptionPane.showMessageDialog(null, "Inseridos com sucesso!", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

